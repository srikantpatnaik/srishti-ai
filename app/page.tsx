  const [autoGenerated, setAutoGenerated] = useState(false)
  const [currentChatMessages, setCurrentChatMessages] = useState<any[]>([])
  const [editedAppCode, setEditedAppCode] = useState<string>("")
  const [isEditing, setIsEditing] = useState(false)
  const [shareMessage, setShareMessage] = useState("")
  const [showScrollButton, setShowScrollButton] = useState(false)
  const [previewImageUrl, setPreviewImageUrl] = useState<string | null>(null)
  const [recentChats, setRecentChats] = useState<{ id: string; title: string; timestamp: number }[]>([])
  const [settingsExpanded, setSettingsExpanded] = useState(false)
  const [currentChatId, setCurrentChatId] = useState<string>("")
  const [chatKey, setChatKey] = useState("0")
  const [messagePreviews, setMessagePreviews] = useState<Map<number, string>>(new Map())
  const previewCacheRef = useRef<Map<number, string>>(new Map())
  const [selectedLanguage, setSelectedLanguage] = useState<string>("")
