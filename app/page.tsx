  const handleSaveFromChat = async (msgIndex: number) => {
    const msg = messages[msgIndex]
    const code = extractCodeFromMessage(msg)
    if (!code) return

    // Use user message (not assistant response) for naming
    const userMsg = messages.slice(0, msgIndex).reverse().find(m => m.role === 'user')
    const userContent = userMsg?.content || msg.content || ''
    let appName = generateAppName(userContent)

    // LLM generates clean 1-2 word app name (non-blocking, saves after)
    let llmNamePromise: Promise<string | null> = Promise.resolve(null)
    try {
      llmNamePromise = (async () => {
        const res = await fetch('/api/chat', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            messages: [
              { role: 'system', content: 'You are a naming assistant. Given a user request to build something, output ONLY a clean 1-2 word app name. No quotes, no explanation. Examples: "make a calculator" → "Calculator", "build a sudoku game" → "Sudoku Game", "create a todo list" → "Todo List".' },
              { role: 'user', content: userContent },
            ],
            isAutonomous: false,
            sessionId,
          }),
        })
        const reader = res.body?.getReader()
        if (!reader) return null
        const dec = new TextDecoder()
        let name = ''
        while (true) {
          const { done, value } = await reader.read()
          if (done) break
          const text = dec.decode(value)
          for (const line of text.split('\n')) {
            if (!line || !line.startsWith('{')) continue
            try {
              const j = JSON.parse(line)
              if (j.type === 'text-delta' && j.textDelta) name += j.textDelta
            } catch {}
          }
          if (name.length > 100) break
        }
        const clean = name.trim().replace(/["'\n\r]/g, '').trim().split(/[\s,]+/).slice(0, 2).join(' ')
        return (clean && clean.length > 0 && clean.length < 30) ? clean : null
      })()
    } catch {}

    const existingApp = savedApps.find(app => app.code === code)
    if (existingApp) return

    const newApp: SavedApp = {
      id: Date.now().toString(),
      name: appName,
      icon: getAppIcon(appName),
      code: code,
      url: "",
      chatMessages: messages,
      createdAt: Date.now()
    }
    await saveAppToDB(newApp)
    const blob = new Blob([code], { type: 'text/html' })
    const url = URL.createObjectURL(blob)
    const appWithUrl = { ...newApp, url }
    setSavedApps(prev => [appWithUrl, ...prev])

    // Update name once LLM responds
    if (llmNamePromise) {
      llmNamePromise.then(llmName => {
        if (llmName && llmName !== appName) {
          setSavedApps(prev => prev.map(a => a.id === newApp.id ? { ...a, name: llmName, icon: getAppIcon(llmName) } : a))
          saveAppToDB({ ...newApp, name: llmName }).catch(() => {})
        }
      })
    }
  }

  const handleDownloadFromChat = (msgIndex: number) => {
    const msg = messages[msgIndex]
    const code = extractCodeFromMessage(msg)
    if (!code) return

    const zip = new JSZip()
    zip.file("index.html", wrapHtml(code))
    zip.generateAsync({ type: "blob" }).then((content) => {
      const url = URL.createObjectURL(content)
      const link = document.createElement('a')
      link.href = url
      link.download = `app-${Date.now()}.zip`
      document.body.appendChild(link)
      link.click()
      document.body.removeChild(link)
      URL.revokeObjectURL(url)
    })
  }

  const handleImageSaveFromChat = async (msgIndex: number) => {
    const msg = messages[msgIndex]
    const imageUrl = messageImages.get(msgIndex) || (msg as any).imageUrl
    if (!imageUrl) return

    const msgContent = msg.content || ''
    const promptMatch = msgContent.match(/IMAGE_REQUEST:\s*(.+)/i)
    const rawPrompt = promptMatch ? promptMatch[1].trim() : 'Image'
    const appName = generateAppName(rawPrompt, "Image")

    const existingApp = savedApps.find(app => app.url === imageUrl)
    if (existingApp) return

    try {
      const response = await fetch(imageUrl)
      const blob = await response.blob()
      const reader = new FileReader()
      reader.onloadend = async () => {
        const base64 = reader.result as string
        const newApp: SavedApp = {
          id: Date.now().toString(),
          name: appName,
          icon: "🖼️",
          code: base64,
          url: imageUrl,
          createdAt: Date.now()
        }
        await saveAppToDB(newApp)
        setSavedApps(prev => [newApp, ...prev])
      }
      reader.readAsDataURL(blob)
    } catch (error) {
      console.error("Failed to save image:", error)
    }
  }

  const handleImageDownloadFromChat = async (msgIndex: number) => {
    const msg = messages[msgIndex]
    const imageUrl = messageImages.get(msgIndex) || (msg as any).imageUrl
    if (!imageUrl) return

    const msgContent = msg.content || ''
    const promptMatch = msgContent.match(/IMAGE_REQUEST:\s*(.+)/i)
    const rawPrompt = promptMatch ? promptMatch[1].trim() : 'image'
    const fileName = generateFileName(rawPrompt)

    const extension = imageUrl.includes('image/png') ? 'png' : 'jpg'
    const link = document.createElement('a')
    link.href = imageUrl
    link.download = `${fileName}-${Date.now()}.${extension}`
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
  }

